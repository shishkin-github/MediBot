SUPPORT_TEMPLATES: dict[SupportBlock, tuple[SupportTemplate, ...]] = {
    block: tuple(
        SupportTemplate(
            id=template_id,
            block=block,
            method_family=method_family,
            tags=tags,
            text=normalize_ui_text(text),
        )
        for template_id, method_family, tags, text in templates
    )
    for block, templates in _RAW_TEMPLATE_DATA.items()
}


def choose_support_template(
    block: SupportBlock,
    history: list[SupportTemplateHistoryEntry],
    rng: random.Random | None = None,
    now: datetime | None = None,
) -> SupportTemplate:
    if rng is None:
        rng = random.Random()
    if now is None:
        now = datetime.now(timezone.utc)

    templates = SUPPORT_TEMPLATES[block]
    recent_ids = [entry.template_id for entry in history[:7]]
    cooldown_threshold = now - timedelta(days=14)
    cooldown_ids = {
        entry.template_id
        for entry in history
        if entry.created_at >= cooldown_threshold
    }
    last_method = history[0].method_family if history else None

    pool = [
        template
        for template in templates
        if template.id not in recent_ids
        and template.id not in cooldown_ids
        and template.method_family != last_method
    ]
    if not pool:
        fallback_recent_ids = {entry.template_id for entry in history[:3]}
        pool = [
            template
            for template in templates
            if template.id not in fallback_recent_ids
        ]
    if not pool:
        pool = list(templates)
    return rng.choice(pool)
